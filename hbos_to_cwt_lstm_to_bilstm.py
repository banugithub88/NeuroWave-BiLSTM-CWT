def Transform_data(data):
    #Encoding Lables into numbers
    encoding_data = ({'NEUTRAL': 0, 'POSITIVE': 1, 'NEGATIVE': 2} )
    data_encoded = data.replace(encoding_data)
    #getting brain signals into x variable
    x=data_encoded.drop(["label"]  ,axis=1)
    #getting labels into y variable
    y = data_encoded.loc[:,'label'].values
    scaler = StandardScaler()
    #scaling Brain Signals
    scaler.fit(x)
    X = scaler.transform(x)
    #One hot encoding Labels
    Y = to_categorical(y)
    return X,Y

#Calling above function and splitting dataset into train and test
X,Y = Transform_data(data)
x_train, x_test, y_train, y_test = train_test_split(X, Y, test_size = 0.2, random_state = 4)

#checking shape of dataset
x_train.shape[1]

def create_model():
    # Input layer of model for brain signals
    inputs = tf.keras.Input(shape=(x_train.shape[1],))

    # Hidden Layer for Brain signal using Bidirectional LSTM
    # Use Keras Lambda layer to wrap tf.expand_dims to add a third dimension
    expand_dims = tf.keras.layers.Lambda(lambda x: tf.expand_dims(x, axis=2))(inputs)

    # Bidirectional LSTM layer with 256 units
    bilstm = tf.keras.layers.Bidirectional(tf.keras.layers.LSTM(256, return_sequences=True))(expand_dims)

    # Flatten the BiLSTM layer into vector form (one-dimensional array)
    flatten = tf.keras.layers.Flatten()(bilstm)

    # Output layer of the model
    outputs = tf.keras.layers.Dense(3, activation='softmax')(flatten)

    # Create the model
    model = tf.keras.Model(inputs=inputs, outputs=outputs)

    # Print the model summary
    print(model.summary())

    return model

# Creating the BiLSTM model
bilstm_model = create_model()

# Compiling the model
bilstm_model.compile(
    optimizer='adam',
    loss='categorical_crossentropy',
    metrics=['accuracy']
)

