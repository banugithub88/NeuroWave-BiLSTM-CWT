import numpy as np
import matplotlib.pyplot as plt
from mpl_toolkits.mplot3d import Axes3D

# Function to generate a sinusoidal surface (representing one optimization landscape)
def sin_surface(X, Y):
    return np.sin(np.sqrt(X**2 + Y**2))

# Function to generate a cosine surface (representing another optimization landscape)
def cos_surface(X, Y):
    return np.cos(np.sqrt(X**2 + Y**2))

# Generate a grid of x and y values
x = np.linspace(-5, 5, 50)
y = np.linspace(-5, 5, 50)
X, Y = np.meshgrid(x, y)

# Compute the corresponding Z values for the surfaces
Z_sin = sin_surface(X, Y)
Z_cos = cos_surface(X, Y)

# Points representing the start (red) and end (purple) of the optimization path
start_point = np.array([1, 1])
end_point = np.array([3, 3])

# Create a figure with subplots arranged vertically
fig = plt.figure(figsize=(8, 15))

# Plot (a): Flat surface with two points connected by a dashed line
ax1 = fig.add_subplot(311, projection='3d')
ax1.scatter(start_point[0], start_point[1], 0, color='red', s=100, label='Start')
ax1.scatter(end_point[0], end_point[1], 0, color='purple', s=100, label='End')
ax1.plot([start_point[0], end_point[0]], [start_point[1], end_point[1]], [0, 0], 'k--')
ax1.set_title('Initial Flat Loss Landscape: Early Stage of BiLSTM Training with CWT Features')
ax1.set_zlim(0, 1)
ax1.legend()

# Plot (b): 3D Sin surface with two points connected by a dashed line
ax2 = fig.add_subplot(312, projection='3d')
ax2.plot_surface(X, Y, Z_sin, cmap='viridis', alpha=0.6, edgecolor='none')
ax2.scatter(start_point[0], start_point[1], sin_surface(start_point[0], start_point[1]), color='red', s=100)
ax2.scatter(end_point[0], end_point[1], sin_surface(end_point[0], end_point[1]), color='purple', s=100)
ax2.plot([start_point[0], end_point[0]], [start_point[1], end_point[1]],
         [sin_surface(start_point[0], start_point[1]), sin_surface(end_point[0], end_point[1])], 'k--')
ax2.set_title('Sinusoidal Loss Landscape: Complex Temporal Pattern Learning in BiLSTM')

# Plot (c): 3D Cos surface with two points connected by a dashed line
ax3 = fig.add_subplot(313, projection='3d')
ax3.plot_surface(X, Y, Z_cos, cmap='viridis', alpha=0.6, edgecolor='none')
ax3.scatter(start_point[0], start_point[1], cos_surface(start_point[0], start_point[1]), color='red', s=100)
ax3.scatter(end_point[0], end_point[1], cos_surface(end_point[0], end_point[1]), color='purple', s=100)
ax3.plot([start_point[0], end_point[0]], [start_point[1], end_point[1]],
         [cos_surface(start_point[0], start_point[1]), cos_surface(end_point[0], end_point[1])], 'k--')
ax3.set_title('Cosine Loss Landscape: Capturing Long-Term and Short-Term Dependencies in CWT-BiLSTM')

# Show the plots
plt.tight_layout()
plt.show()

